#include <stdio.h>

int calculate(int a, int b)
{
	int temp;

	if (a < b)
	{
		temp = b;
		b = a;
		a = temp;
	}

	while (b != 0)
	{
		temp = a % b;
		a = b;
		b = temp;
	}

	return a;
}

int main()
{
	int x, y, z;
	scanf("%d,%d", &x, &y);
	z = calculate(x, y);
	printf("%d", z);
	return 0;
}
